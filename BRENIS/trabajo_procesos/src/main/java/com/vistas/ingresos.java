     private void cargarDatosEnTabla(){
        List<Ingreso> listaIngresos = ListaIngresos();
        DefaultTableModel model = (DefaultTableModel) Tabla_Ingreso.getModel();
        model.setRowCount(0);
            for(Ingreso i: listaIngresos){
                model.addRow(new Object[]{
                    i.getId_ingreso(),
                    i.getTipo_pago(),
                    i.getDia_ingreso(),
                    i.getMes_ingreso(),
                    i.getA_ingreso(),
                    i.getDpto(),
                    i.getMonto(),
                    i.getDNI_encargado()
                });
            }
    }

