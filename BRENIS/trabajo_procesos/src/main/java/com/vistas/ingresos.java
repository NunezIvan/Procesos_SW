    private void Modificar_ingreso_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Modificar_ingreso_btnActionPerformed
         int selectedRow = Tabla_Ingreso.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un registro valido", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int idIngreso = (int) Tabla_Ingreso.getValueAt(selectedRow, 1);
        String tipo_Ingreso = (String) Tabla_Ingreso.getValueAt(selectedRow, 1);
        Ingreso ingresoSeleccionado = new Ingreso();
        ingresoSeleccionado.setId_ingreso(idIngreso);
        if(!Ingresos.buscarIngreso_ID(ingresoSeleccionado)){
            JOptionPane.showMessageDialog(this, "No se pudo encontrar el ingreso en la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JDialog dialog = new JDialog((JFrame) SwingUtilities.getWindowAncestor(this), "Modificar Ingreso", true);
            Modificar_Ingreso modificarIngresoPanel = new Modificar_Ingreso(ingresoSeleccionado);
            modificarIngresoPanel.cargarDatosEnCampo();
            modificarIngresoPanel.setDialog(dialog);
            dialog.add(modificarIngresoPanel);
            dialog.setSize(300, 443);
            dialog.setLocationRelativeTo(null);
            dialog.setVisible(true);
            cargarDatosEnTabla();
            
    }//GEN-LAST:event_Modificar_ingreso_btnActionPerformed

    private void Eliminar_ingreso_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Eliminar_ingreso_btnActionPerformed
        int selectedRow = Tabla_Ingreso.getSelectedRow();
        if (selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un registro valido", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Â¿Esta seguro que desea eliminar el ingreso?", "Confirmar eliminacion", JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION){
            int idIngreso = (int) Tabla_Ingreso.getValueAt(selectedRow, 0);
            Ingreso ingresoSeleccionado = new Ingreso();
            ingresoSeleccionado.setId_ingreso(idIngreso);
            boolean eliminar = EliminarIngreso(ingresoSeleccionado);
            if(eliminar){
                JOptionPane.showMessageDialog(this, "Ingreso eliminado exitosamente", "Exito", JOptionPane.INFORMATION_MESSAGE);
                cargarDatosEnTabla();
            }else{
                JOptionPane.showMessageDialog(this, "Error al eliminar el ingreso", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_Eliminar_ingreso_btnActionPerformed

